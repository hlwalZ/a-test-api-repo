app.MapGet("/PokemonList", async () =>
{


    HttpClient client = new HttpClient();
    HttpResponseMessage resu = await client.GetAsync("https://pokeapi.co/api/v2/pokemon?limit=300");
    resu.EnsureSuccessStatusCode();



    string responsess = await resu.Content.ReadAsStringAsync();
    List<PokemonList>? pokes = JsonSerializer.Deserialize<JsonElement>(responsess).GetProperty("results").Deserialize<List<PokemonList>>();



    return pokes;
})
.WithName("PokemonList")
.WithOpenApi();


app.Run();

